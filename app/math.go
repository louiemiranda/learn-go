package main

// Add func
func Add(x int, y int) int {
	// fmt.Println("Result: x=%d + y=%d", x, y)
	return x + y
}

// Subtract func
func Subtract(x int, y int) int {
	// fmt.Println("Result: x=%d - y=%d", x, y)
	return x - y
}

// Multiply func
func Multiply(x int, y int) int {
	return x * y
}

// Divide func
func Divide(x int, y int) int {
	return x * y
}
